`timescale 1ns / 1ps


// ============================================================
// 模組名稱：mod10_T_FF
// 功能說明：
//   1) 使用 4 個 T 觸發器（T_FF）實作「同步」mod10 計數器。
//   2) 計數序列為：0 -> 1 -> 2 -> ... -> 9 -> 0 -> ...
//   3) 外部提供非同步低有效重置 rst_n：
//      - rst_n = 0 時，計數器立即清為 0。
//   4) 具備非法狀態保護：
//      - 若因干擾進入 10~15，下一個時脈會自動回到 0。
//
// 設計重點：
//   - T 觸發器狀態方程：Q_next = Q xor T
//   - 當需要「清為 0」時，令 T = Q，即可在下一拍得到 Q_next = 0。
// ============================================================
module mod10_T_FF (
	input  wire       clk,     // 系統時脈（正緣觸發）
	input  wire       rst_n,   // 非同步低有效重置
	output wire [3:0] q        // 4-bit 計數輸出（q[3]為MSB，q[0]為LSB）
);

	// --------------------------------------------------------
	// 內部目前狀態（由 4 個 T_FF 提供）
	// --------------------------------------------------------
	wire q0, q1, q2, q3;

	// --------------------------------------------------------
	// 狀態偵測
	// --------------------------------------------------------
	// is_9：當前狀態是否為十進位 9 (二進位 1001)
	wire is_9 = q3 & ~q2 & ~q1 & q0;

	// invalid_state：是否落在非法 BCD 狀態（10~15）
	// 10~15 的共同條件可化簡為 q3=1 且 (q2=1 或 q1=1)
	wire invalid_state = q3 & (q2 | q1);

	// force_zero：需要在下一拍把狀態清成 0000 的條件
	// 1) 到達 9 後，下一拍應回到 0
	// 2) 進入非法狀態時，下一拍自動修正回 0
	wire force_zero = is_9 | invalid_state;

	// --------------------------------------------------------
	// 各 bit 的 T 輸入邏輯
	// --------------------------------------------------------
	// 正常二進位遞增（0~8）時：
	//   t0 = 1
	//   t1 = q0
	//   t2 = q1 & q0
	//   t3 = q2 & q1 & q0
	//
	// 若 force_zero=1，令 tN=qN，則下一拍 qN_next = qN xor qN = 0。
	// 這可讓狀態在下一個時脈統一回到 0000。
	wire t0 = force_zero ? q0 : 1'b1;
	wire t1 = force_zero ? q1 : q0;
	wire t2 = force_zero ? q2 : (q1 & q0);
	wire t3 = force_zero ? q3 : (q2 & q1 & q0);

	// --------------------------------------------------------
	// 4 個 T 觸發器（共用同一個 clk，形成同步計數器）
	// q_bar 在本設計中未使用，故留空連接。
	// --------------------------------------------------------
	T_FF u_ff0 (
		.clk   (clk),
		.rst_n (rst_n),
		.t     (t0),
		.q     (q0),
		.q_bar ()
	);

	T_FF u_ff1 (
		.clk   (clk),
		.rst_n (rst_n),
		.t     (t1),
		.q     (q1),
		.q_bar ()
	);

	T_FF u_ff2 (
		.clk   (clk),
		.rst_n (rst_n),
		.t     (t2),
		.q     (q2),
		.q_bar ()
	);

	T_FF u_ff3 (
		.clk   (clk),
		.rst_n (rst_n),
		.t     (t3),
		.q     (q3),
		.q_bar ()
	);

	// 將內部位元依 [3:0] 封裝成輸出匯流排
	assign q = {q3, q2, q1, q0};

endmodule